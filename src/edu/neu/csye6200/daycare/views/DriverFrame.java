    public void startDeadCheckStu(String fname, String lname, String id) throws InterruptedException{
        SwingWorker<Void,String> worker = new SwingWorker<Void, String>() {
    
           
            @Override
            protected Void doInBackground() throws Exception {
                File file = new File(FILE_PATH_DEADLINE);
                String line="";
                
                
                
                try {
                    BufferedReader br = new BufferedReader(new FileReader(file));
                    while((line=br.readLine())!=null){
              
              
                    String[] stu = line.split(",");
              
                    int updatedDead = Integer.parseInt(stu[5]);
              
              
                    if(stu[0].equals(id)&& stu[1].equals(fname)&&stu[2].equals(lname)){
                        
                    if(updatedDead == 0 || updatedDead < 0){
                        String send = stu[1]+" "+stu[2];
                
                       publish(send);
                    }
                     
                    }
              
                }
          }catch(IOException e){
            System.out.println(e);
        }
                
                
        return null;
        }

            @Override
            protected void process(List<String> chunks) {
                String val = chunks.get(chunks.size()-1); 
                immuStatus.append(val+"\n");
            }
            
        }; 
  
            worker.execute(); 
 
    }
    
     public void startDeadCheckAdmin() throws InterruptedException{
         
         List<String> stuList = new ArrayList<>();
        SwingWorker<Void,List<String>> worker = new SwingWorker<Void, List<String>>() {
           
           
            @Override
            protected Void doInBackground() throws Exception {
                File file = new File(FILE_PATH_DEADLINE);
                String line="";
                
                
                
                try {
                    BufferedReader br = new BufferedReader(new FileReader(file));
                    while((line=br.readLine())!=null){
              
              
                    String[] stu = line.split(",");
              
                    int updatedDead = Integer.parseInt(stu[5]);
              
              
                    if(updatedDead == 0 || updatedDead < 0){
                        String send = stu[1]+" "+stu[2];
                        stuList.add(send);
                       
                    }
                    
                     publish(stuList);
              
                }
                    
                    
          }catch(IOException e){
            System.out.println(e);
        }
        return null;
        }

            @Override
            protected void process(List<List<String>> chunks) {
                List<String> val = chunks.get(chunks.size()-1);
                
                for(String s:val){
                    immuStatus.append(s+"\n");
                }
                
            }
            
       
        }; 
  
            worker.execute(); 
            
      
 
    }

    
