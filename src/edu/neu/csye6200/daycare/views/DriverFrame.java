    public void setBtns(boolean loggedin,String role){
        if(loggedin && role.equals("admin")){
            AdminBtn.setEnabled(true);
            LoginBtn.setEnabled(false);
            LogoutBtn.setEnabled(true);
            StudBtn.setEnabled(true);
            TeachrBtn.setEnabled(true);
            chatButton.setEnabled(true);
        }else if(loggedin && role.equals("student")){
            AdminBtn.setEnabled(false);
            LoginBtn.setEnabled(false);
            LogoutBtn.setEnabled(true);
            StudBtn.setEnabled(false);
            TeachrBtn.setEnabled(false);
            chatButton.setEnabled(true);
        } else if(loggedin && role.equals("teacher")){
            AdminBtn.setEnabled(false);
            LoginBtn.setEnabled(false);
            LogoutBtn.setEnabled(true);
            StudBtn.setEnabled(true);
            TeachrBtn.setEnabled(false);
            chatButton.setEnabled(false);
        }
        else{
            AdminBtn.setEnabled(false);
            LoginBtn.setEnabled(true);
            LogoutBtn.setEnabled(false);
            StudBtn.setEnabled(false);
            TeachrBtn.setEnabled(false);
            chatButton.setEnabled(false);
            
        }
    }

